#! /bin/bash

#https://github.com/Timmy-Edibo/bash-scripts

a=40
b=4

# Addition
echo "Addition of $a+$b = "$[a+b]

#Multiplication
echo "Multiplication of $a*$b = " $[a*b]

#Subtraction
echo "Subtraction of $a-$b =" $[a-b]


#Division
echo "Division of $a/$b = " $[a/b]

#Modulus or reminder
echo "reminder of $a%$b = " $[a%b]

